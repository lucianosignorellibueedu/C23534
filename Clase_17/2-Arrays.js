let array = [];
console.log(array);
// .push(obj1, obj2...) Añade uno o varios elementos al final del array. Devuelve tamaño del array.
array.push('Pedro');
console.log(array);
array.push('Maria');
console.log(array);
array.push(2);
console.log(array);
array.push('Rosario');
console.log(array);



// .pop() Elimina y devuelve el último elemento del array.
array.pop();
console.log(array);
array.pop();
console.log(array);
// .unshift(obj1, obj2...) Añade uno o varios elementos al inicio del array. Devuelve tamaño del array.
array.unshift('Leo');
console.log(array);
array.unshift('Dibu');
console.log(array);
array.push('Rosario');
console.log(array);
// .shift() Elimina y devuelve el primer elemento del array.
array.shift();
